#include <iostream>
using namespace std;
int main() {
    // Declaring variables of all primitive data types
    int int_var = 10;
    float float_var = 3.14f;
    double double_var = 1234.5678;
    char char_var = 'A';
    bool bool_var = true;
    
    // Printing the values
    cout << "int_var: " << int_var << endl;
    cout << "float_var: " << float_var << endl;
    cout << "double_var: " << double_var << endl;
    cout << "char_var: " << char_var << endl;
    cout << "bool_var: " << bool_var << endl;

    return 0;
}
